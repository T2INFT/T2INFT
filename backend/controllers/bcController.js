export const mint = async (req, res) => {
    let dataurl;
    // TODO: get user wallet from database, get private key from front-end
    // private key communication need encryption?
	const userid = req.body.userid;
    if (!userid) {
        return res.status(400).json({ message: "User ID is missing" });
    }

    const user = await User.findByPk(userid);
    if (!user) {
        return res.status(400).json({ message: "User not found" });
    }
	
    const me = { address: req.body.address, privateKey: req.body.privateKey };

	const prompt = req.body.prompt;

    try {
		const image = await fileFromPath(req.file.path, req.file.mimetype);
		if (!image) {
			return res.status(400).json({ message: "Image upload failed" });
		}

        const metadata = await storage.store({
			name: user.username,
			description: prompt,
            image: image
        });

        if (!metadata || !metadata.url) {
            return res.status(400).json({ message: "NFT Storage failed" });
        }

        dataurl = metadata.url;
    } catch (error) {
        res.status(500).json({ success: false, error: error.message });
    }

    try {
        var result = await web3.eth.sendSignedTransaction((await me.signTransaction({
            input: contract.methods.mint(me.address, dataurl).encodeABI(),
            to: contractAddress,
            from: me.address,
            gas: 1000000,
        })).rawTransaction);

        if (!result) {
            res.status(500).json({ success: false, error: "Transaction failed" });
        }

        return res.status(200).json({ success: true, data: result });
    } catch (error) {
        res.status(500).json({ success: false, error: error.message });
    }
};
